# int를 사용하면 정수형으로 형변환을 할 수 있다.
print(int(3.8))
print(int("2") + int("5"))

# float을 사용하면 소수형으로 형변환을 할 수 있다.
print(float(3))
print(float("1.1") + float("2.5"))

# str을 사용하면 문자열 형변환을 할 수 있다.
print(str(2) + str(5))

age = 7
print("제 나이는 " + str(age) + "살 입니다.")

